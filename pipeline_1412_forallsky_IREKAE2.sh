function do_kando_hosei
{
    #物理的原因がよく分かっていない感度補正(天筒)
    
    ####  月を見た後の感度補正  ####
    #リストからパラメータ抜き出し
    search_ccsdsti=$ccsdsti
    if [ $search_ccsdsti -lt 1000000000 ] ; then
	search_ccsdsti=" "$ccsdsti
    fi
    smoon_listfile=${bin_dir}"smoon_list2"
    if [ -f smoon_list2_mini ] ; then
	smoon_listfile=smoon_list2_mini
    fi

    smoon_data=$(grep -m1 ^"$search_ccsdsti" $smoon_listfile)
    if [ "${smoon_data}" = "" ]; then
	smoon_listfile=${bin_dir}"smoon_list2"
	smoon_data=$(grep -m1 ^"$search_ccsdsti" $smoon_listfile)
    fi
    smoon=$(echo $smoon_data|cut -d ',' -f 2)
    minute_aftermoon=$(echo $smoon_data|cut -d ',' -f 3)

    #パラメータで補正が必要か判断
    do_smoon_correct=0
    do_smoon_correct=$(echo "( $smoon * $smoon < 100 ) && ( $minute_aftermoon < 204)"|bc)
    if [ $do_smoon_correct -eq 1 ] ; then
	smoon_coef_listfile=${bin_dir}"smoon_coeff_"${ch}"_2.list"
	sp2_aftermoon ${prefix}.fits $smoon $minute_aftermoon $smoon_coef_listfile ${prefix}.fits
    fi

    ####  Lの感度が時間変化しているのを補正  ####
    if [ "$ch" = "L" ] ; then
	iyear=$(echo $datestr|cut -c 1-4)
	imonth=$(echo $datestr|cut -c 6-7)
	iday=$(echo $datestr|cut -c 9-10)
	ihour=$(echo $timestr|cut -c 1-2)
	year=$(echo "scale=3; ($imonth -1.) / 12.+($iday -1. +$ihour /24.) /365.+$iyear"|bc)
	sp2_l1kando_1412 ${prefix}.fits $year ${prefix}.fits
	# year=$(echo "scale=3;$imonth / 12.+($iday+$ihour /24.) /365.+$iyear"|bc)
	# sp2_l1kando ${prefix}.fits $year ${bin_dir}year_and_relstr_L.txt ${prefix}.fits
    fi
}

function do_sext_scon66
{
    # 2行別々にSource Extractionを行う
    sex -c sep11S.sex ${prefix}.fits >& /dev/null
    cp test1.cat ${prefix}_sep.cat

    # Background(Source抜き)強度を取得
    str=(`sp2_d2f ${prefix}.fits check_sep.fits ${prefix}_bg.fits`);
      bg1=${str[0]};
     nbg1=${str[1]};
      bg2=${str[2]};
     nbg2=${str[3]};
      bgA=${str[4]};
     nbgA=${str[5]};
      bgB=${str[6]};
     nbgB=${str[7]};
      bgC=${str[8]};
     nbgC=${str[9]};

     # Stray light 開発用 routine
#     q_list_stat ${prefix}_bg.fits 1 4-60 200:300 1 | awk '{print $2,$5}' > l
#     str=(`sh sp2_fit.sh`);
#     c1=${str[0]};
#     c2=${str[1]};
#     c3=${str[2]};

    # SatTrail中にいる天体は除外
    sp2_satrej.pl ${prefix}_sep.cat ${prefix}_satur.dat  ${prefix}_sep.cat

    # HKをHeaderに入れる
    pr2_fitshd080933 ${prefix}.fits ${prefix}.fits > ${prefix}.pos 2> /dev/null

    # 6x6FITSに合成する
    #1305:flatのウェイトを追加 BADPIXELが補正されます
    sp2_comb33 ${prefix}.fits ${prefix}_mix.fits 6
    cp ${prefix}_mix.fits nokosu.fits ###

    # 6x6合成FITSからSource Extractionを行う
    sex -c mix66S.sex ${prefix}_mix.fits >& /dev/null
    cp test1.cat ${prefix}_mix.cat

    # Seconds confirmation
    #str=(`sp2_scon_6to1B.pl ${prefix}_mix.cat ${prefix}_sep.cat ${prefix}_scon.cat ${prefix}_rej1.cat ${prefix}_rej2.cat $ch $dhuti $ccsdsti`);
    str=(`sp2_scon_6to1C.pl ${prefix}_mix.cat ${prefix}_sep.cat ${prefix}_scon.cat ${prefix}_rej1.cat ${prefix}_rej2.cat $ch $dhuti $ccsdsti`);
     nsrc=${str[0]};  # Seconds confirmed 天体の数
    nrej1=${str[1]};  # Rejectされたrow#1のイベントの数
    nrej2=${str[2]};  # Rejectされたrow#2のイベントの数
    nbright=${str[3]};# Bright Sourceの数。

    # カメラ内Stray light 除去:背景
    case "$ch" in
	L)
	    sp2_subscat080930 ${prefix}.fits $bg1 ${prefix}.fits
	    #眩しい光源一覧を見る
	    if [ -f BrightPrefix.list ]; then
		nbright_listed=$(grep -s $prefix "BrightPrefix.list"|wc -l)
		nbright=$(echo "$nbright + $nbright_listed"|bc)

		#カメラ内Stray light 除去:点源
		if [ $nbright -gt 0 ];
		    then
		    cat <<EOF|idl -queue
.com /usr/mirmap/bin/diffusescat/diffuse_remscat
.com /usr/mirmap/bin/PSF_FITTER/diffuse_psf_fitter
.com /usr/mirmap/bin/PSF_FITTER/diffuse_psf_cs
diffuse_remscat,'${prefix}.fits',BRIGHT_LIST="BrightPrefix.list"
exit
EOF
		fi
	    fi
	;;
	*)
	    echo -n "";
	;;
    esac

    sp3_cray2msk.pl cray > ${prefix}_cray.dat

    # Rejectされたeventを塗り潰して、FITSを作り直す
    sp2_comb33_c ${prefix}.fits ${prefix}_mix.fits 6 cray
    #sp2_comb33_1305がフラットのウェイト適用
    
    # 受かった時の積分値情報を追加 (12/28 NEW! 要注意)
    sp2_scon_intg.pl ${prefix}_scon.cat ${prefix}_scon.cat

    debug_stdout
}


# Pipe-lineの入力データをdbからとってくる
function do_get_rawdata_db
{
    prefix=$1;

    # 前ver.との互換性のため文字列分解
    #  prefix=F00XXXX_4NS => fid=XXXX、ch=S
    fid=${prefix#F}; fid=${fid%_4NS}; fid=${fid%_4NL};
    ch=${prefix#F${fid}}; ch=${ch#_4N};
    
    dbget_rawfits3.pl $fid 4N $ch;
    if [ -f ${prefix}.fits ]; then
	echo -n "";
    else
	echo "ERR: ${prefix} not found in DB";
	exit -1;
    fi
}


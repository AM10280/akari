# 変数を保存する
function do_save_vals
{
   export LANG=C; cur_date_str=`date`;
   printf "#-------------------------------------------------------------#\n" > ${prefix}.cache
   echo "# ${prefix}.cache"           >> ${prefix}.cache;
   echo "# Generated by $pipeline"    >> ${prefix}.cache;
   echo "#           at $cur_date_str">> ${prefix}.cache;
   echo "#           by $USER@$HOST"  >> ${prefix}.cache;

   printf "#--- FITS ----------------------------------------------------#\n" >> ${prefix}.cache
   printf "prefix=\"$prefix\" # Prefix (ex) F0000001_4NS\n" >> ${prefix}.cache
   printf "ch=\"$ch\"                # Channel S/L\n" >> ${prefix}.cache
   printf "fid=$fid        # FrameID\n" >> ${prefix}.cache
   printf "dhuti=$dhuti      # 36(64)bit TI \n" >> ${prefix}.cache
   printf "ccsdsti=$ccsdsti     # 32bit TI\n" >> ${prefix}.cache
   printf "datastr=\"$datestr\"  # Date in YYYY-MM-DD format\n" >> ${prefix}.cache
   printf "timestr=\"$timestr\"    # Time in HH:MM:SS format\n" >> ${prefix}.cache
   printf "rstm=\"Long\"           # Reset mode\n" >> ${prefix}.cache
   printf "rstp=\"306\"            # Reset period\n" >> ${prefix}.cache

   printf "#--- Status --------------------------------------------------#\n" >> ${prefix}.cache
   printf "rst1=%-8.1f  # Row#1 reset level (e)\n"      $rst1  >> ${prefix}.cache;
   printf "nrst1=%-8.1f # Row#1 noise of reset (e)\n"   $nrst1 >> ${prefix}.cache;
   printf "rst2=%-8.1f  # Row#2 reset level (e)\n"      $rst2  >> ${prefix}.cache;
   printf "nrst2=%-8.1f # Row#2 noise of reset (e)\n"   $nrst2 >> ${prefix}.cache;
   printf "bg1=%-8.3f   # Row#1 background level (e)\n" $bg1   >> ${prefix}.cache;
   printf "nbg1=%-8.3f  # Row#1 background noise (e)\n" $nbg1  >> ${prefix}.cache;
   printf "bg2=%-8.3f   # Row#2 background level (e)\n" $bg2   >> ${prefix}.cache;
   printf "nbg2=%-8.3f  # Row#2 background noise (e)\n" $nbg2  >> ${prefix}.cache;
   printf "msk1=%-8.3f  # Row#1 slit-mask level (e)\n"  $msk1  >> ${prefix}.cache;
   printf "nmsk1=%-8.3f # Row#1 slit-mask noise (e)\n"  $nmsk1 >> ${prefix}.cache;
   printf "msk2=%-8.3f  # Row#2 slit-mask level (e)\n"  $msk2  >> ${prefix}.cache;
   printf "nmsk2=%-8.3f # Row#2 slit-mask noise (e)\n"  $nmsk2 >> ${prefix}.cache;

   printf "#--- Status2 --------------------------------------------------#\n" >> ${prefix}.cache
   printf "bgA=%-8.3f # \n"  $bgA >> ${prefix}.cache;
   printf "nbgA=%-8.3f # \n"  $nbgA >> ${prefix}.cache;
   printf "bgB=%-8.3f # \n"  $bgB >> ${prefix}.cache;
   printf "nbgB=%-8.3f # \n"  $nbgB >> ${prefix}.cache;
   printf "bgC=%-8.3f # \n"  $bgC >> ${prefix}.cache;
   printf "nbgC=%-8.3f # \n"  $nbgC >> ${prefix}.cache;
   printf "c1=%-8.3f # \n"  $c1 >> ${prefix}.cache;
   printf "c2=%-8.3f # \n"  $c2 >> ${prefix}.cache;
   printf "c3=%-8.3f # \n"  $c3 >> ${prefix}.cache;
   printf "craA=%-8.3f # \n"  $craA >> ${prefix}.cache;
   printf "cdeA=%-8.3f # \n"  $cdeA >> ${prefix}.cache;
   printf "craB=%-8.3f # \n"  $craB >> ${prefix}.cache;
   printf "cdeB=%-8.3f # \n"  $cdeB >> ${prefix}.cache;
   printf "craC=%-8.3f # \n"  $craC >> ${prefix}.cache;
   printf "cdeC=%-8.3f # \n"  $cdeC >> ${prefix}.cache;

   printf "#--- Events --------------------------------------------------#\n" >> ${prefix}.cache;
   printf "nsrc=%-4d      # Num of seconds confirmed events (Source)\n" $nsrc  >> ${prefix}.cache;
   printf "nrej1=%-4d     # Row#1 num of rejected events (Glitch)\n"    $nrej1 >> ${prefix}.cache;
   printf "nrej2=%-4d     # Row#2 num of rejected events (Glitch)\n"    $nrej2 >> ${prefix}.cache;

   printf "#--- WCS -----------------------------------------------------#\n" >> ${prefix}.cache;
   printf "cra=$cra   # ra  in J2000 decimal\n"  >> ${prefix}.cache;
   printf "cde=$cde   # dec in J2000 decimal\n"  >> ${prefix}.cache;
   printf "xshift=%-16.9f   # dRA correction\n" $xshift >> ${prefix}.cache;
   printf "yshift=%-16.9f   # dDec correction\n" $yshift >> ${prefix}.cache;
   printf "n_msx=%-4d     # Num of MSX cross-ID\n" $n_msx >> ${prefix}.cache;
   printf "n_iras=%-4d    # Num of IRAS cross-ID\n" $n_iras >> ${prefix}.cache;
   printf "n_tmass=%-4d   # Num of 2MASS(<8mag) cross-ID\n" $n_tmass >> ${prefix}.cache;
   printf "calc=%-2d       # 1:Local P.R. done   0:Not done\n" $calc >> ${prefix}.cache;
   printf "#-------------------------------------------------------------#\n" >> ${prefix}.cache;
}


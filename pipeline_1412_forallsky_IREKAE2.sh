# ADU→e変換、double化、Resetレベルのチェック
# dhuti ccsdsti ch datestr timestr rst1 nrst1 rst2 nrst2 も取得
function do_adu2es
{
    prefix=$1;
    
    # ファイルがあることを確認
    if [ -f ${prefix}.fits ]; then echo -n "";
    else echo "Fatal at do_adu2es(): ${prefix}.fits not found" 1>&2; exit -1; fi

    # ADU→e変換する。ついでにHeaderから変数取得。
    str=(`sp2_adu2es ${prefix}.fits ${prefix}.fits`);

      dhuti=${str[0]};
    ccsdsti=${str[1]};
         ch=${str[2]};
    datestr=${str[3]};
    timestr=${str[4]};

       itg1=${str[5]};
      nitg1=${str[6]};
       itg2=${str[7]};
      nitg2=${str[8]};

       rst1=${str[9]};
      nrst1=${str[10]};
       rst2=${str[11]};
      nrst2=${str[12]};

      nzero=${str[9]};

    # 0 level の pixelが10以上あったらFITSが壊れている恐れがある
    if [ $nzero -gt 10 ];then
	echo "Fatal: nzero=$nzero for ${prefix}: please check";
	exit -1;
    fi
}

